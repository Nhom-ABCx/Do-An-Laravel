     */
    //đăng nhập nè
    public function store(Request $request)
    {
        $credentials = $request->validate([
            'Username' => ['required'],
            'MatKhau' => ['required'],
        ]);

        if (Auth::attempt($credentials)) {
            $request->session()->regenerate();

            return redirect()->intended('/');
        }

        return back()->withErrors([
            'Username' => 'Loi dang nhap',
        ]);
    }

